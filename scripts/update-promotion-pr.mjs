#!/usr/bin/env node
/**
 * Maintains a single rolling dev → main PR. Run locally or from
 * .github/workflows/rolling-dev-main-pr.yml after each push to dev.
 *
 * Requires: git, gh (authenticated), full git history (fetch-depth: 0).
 */

import { execFileSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { join } from 'node:path'
import { CHECKBOX_RE, extractSection, findCheckedInLines, splitIncludedByPr, findUncheckedInLines } from './lib/promotion-checklist.mjs'

const POST_MERGE_RE = /^post-merge:/i

const PROMOTION_TITLE_PREFIX = '[promotion] dev → main'
const PROMOTION_LABEL = 'promotion'

const PRODUCTION_CHECKLIST_ITEMS = [
  'Every merged feature PR listed below has its staging test plan checked off',
  '`kubectl -n link-apps rollout status deployment/boop-watch-dev --timeout=180s`',
  'Staging `/health` and relevant APIs exercised (port-forward or exec)',
  'Diff scope matches what was verified on staging',
  'Post-merge: `kubectl -n link-apps rollout status deployment/boop-watch --timeout=180s`',
  'Post-merge: smoke `https://watch.boopurno.es/health` and spot-check affected surfaces',
]

// Manually checking a box (or `gh pr edit`) is the only way this PR's
// checklist state changes day to day — the body is otherwise fully
// regenerated on every dev push. Carry forward whatever was checked on the
// previous version of the PR, keyed by the checkbox's exact text, so
// staging verification someone already did doesn't get silently wiped.
function parsePreviousState(body) {
  const checklistChecked = findCheckedInLines(extractSection(body, '## Production promotion checklist'))
  const includedChecked = new Map()
  for (const [prNumber, { lines }] of splitIncludedByPr(extractSection(body, '## Included changes'))) {
    includedChecked.set(prNumber, findCheckedInLines(lines))
  }
  return { checklistChecked, includedChecked }
}

function rewriteCheckboxLine(line, checkedSet) {
  const m = line.match(CHECKBOX_RE)
  if (!m) return line
  const [, mark, text] = m
  const isChecked = mark.trim() !== '' || checkedSet?.has(text.trim())
  return `- [${isChecked ? 'x' : ' '}] ${text}`
}

function sh(cmd, args = []) {
  return execFileSync(cmd, args, { encoding: 'utf8' }).trim()
}

function shJson(cmd, args = []) {
  return JSON.parse(sh(cmd, args))
}

// Editor/agent signature footers ("Made with Cursor", "🤖 Generated with
// Claude Code", "_Generated by Claude Code_") that ride along at the end of
// a feature PR's body — noise once copied into the aggregated checklist.
const ATTRIBUTION_LINE_RE = /^_?(🤖\s*)?(made with|generated (with|by))\s*\[.+?\]\(.+?\)_?\.?$/i

function stripAttributionFooter(text) {
  const lines = text.split(/\r?\n/).filter((l) => !ATTRIBUTION_LINE_RE.test(l.trim()))
  while (lines.length && (lines[lines.length - 1].trim() === '' || lines[lines.length - 1].trim() === '---')) {
    lines.pop()
  }
  while (lines.length && lines[0].trim() === '') lines.shift()
  return lines.join('\n')
}

function extractTestPlan(body) {
  if (!body) return null
  const match = body.match(/## Test plan\r?\n([\s\S]*?)(?:\r?\n## |\s*$)/i)
  if (!match) return null
  const section = stripAttributionFooter(match[1])
  return section || null
}

function readVersion() {
  try {
    const pkg = JSON.parse(readFileSync('package.json', 'utf8'))
    return typeof pkg.version === 'string' ? pkg.version : 'unknown'
  } catch {
    return 'unknown'
  }
}

function buildBody({ version, ahead, commitLines, mergedPrs, previous }) {
  const lines = [
    'Automated rolling promotion PR. **Do not merge until staging is verified** for every included change.',
    '',
    `Staging image: \`:dev\` on \`boop-watch-dev\` · target prod: \`watch.boopurno.es\` · dev version: \`${version}\``,
    '',
    `**${ahead}** commit(s) on \`dev\` ahead of \`main\`.`,
    '',
    '## Production promotion checklist',
    '',
    'Complete after staging verification for all changes below:',
    '',
    ...PRODUCTION_CHECKLIST_ITEMS.map((text) => `- [${previous.checklistChecked.has(text) ? 'x' : ' '}] ${text}`),
    '',
    '## Included changes',
    '',
  ]

  if (mergedPrs.length > 0) {
    for (const pr of mergedPrs) {
      lines.push(`### [#${pr.number}](${pr.url}) ${pr.title}`)
      lines.push('')
      const plan = extractTestPlan(pr.body)
      if (plan) {
        const checkedSet = previous.includedChecked.get(pr.number)
        const preservedPlan = plan
          .split(/\r?\n/)
          .map((line) => rewriteCheckboxLine(line, checkedSet))
          .join('\n')
        lines.push('<details open>')
        lines.push('<summary>Staging test plan (from feature PR — verify on staging before prod)</summary>')
        lines.push('')
        lines.push(preservedPlan)
        lines.push('')
        lines.push('</details>')
        lines.push('')
      } else {
        lines.push('_No test plan section in the feature PR._')
        lines.push('')
      }
    }
  } else {
    lines.push('_No PR-linked commits (merge or squash) in this range — direct commits on dev:_')
    lines.push('')
    for (const line of commitLines) lines.push(line)
    lines.push('')
  }

  if (mergedPrs.length > 0 && commitLines.length > 0) {
    lines.push('## Commits')
    lines.push('')
    for (const line of commitLines) lines.push(line)
    lines.push('')
  }

  lines.push('---')
  lines.push('_Updated automatically when `dev` advances. Edit the checklist on this PR only._')

  return lines.join('\n')
}

function findOpenPromotionPr() {
  const prs = shJson('gh', [
    'pr',
    'list',
    '--base',
    'main',
    '--head',
    'dev',
    '--state',
    'open',
    '--json',
    'number,title,labels,body,isDraft',
  ])
  return prs.find((p) => p.title.startsWith(PROMOTION_TITLE_PREFIX) || p.labels?.some((l) => l.name === PROMOTION_LABEL)) ?? prs[0] ?? null
}

function ensureLabel() {
  try {
    sh('gh', [
      'label',
      'create',
      PROMOTION_LABEL,
      '--color',
      '1d76db',
      '--description',
      'Rolling dev→main production promotion',
    ])
  } catch {
    // already exists
  }
}

function main() {
  sh('git', ['fetch', 'origin', 'main', 'dev'])

  const ahead = Number(sh('git', ['rev-list', '--count', 'origin/main..origin/dev']))
  const openPr = findOpenPromotionPr()

  if (ahead === 0) {
    if (openPr) {
      sh('gh', ['pr', 'close', String(openPr.number), '--comment', 'Closing: `dev` is even with `main` — nothing to promote.'])
      console.log(`Closed promotion PR #${openPr.number} (dev even with main)`)
    } else {
      console.log('dev is even with main; no promotion PR needed')
    }
    return
  }

  // PRs land on dev two ways: a merge commit ("Merge pull request #N") or a
  // squash merge, whose subject GitHub suffixes with "(#N)". Scan both — for
  // a while only merge commits were parsed, so squash-merged PRs' test plans
  // silently skipped the promotion checklist.
  const mergeSubjects = sh('git', ['log', 'origin/main..origin/dev', '--merges', '--pretty=format:%s']).split('\n').filter(Boolean)
  const squashSubjects = sh('git', ['log', 'origin/main..origin/dev', '--no-merges', '--pretty=format:%s']).split('\n').filter(Boolean)
  const prNumbers = [
    ...new Set([
      ...mergeSubjects
        .map((s) => s.match(/Merge pull request #(\d+)/))
        .filter((m) => m !== null)
        .map((m) => Number(m[1])),
      ...squashSubjects
        .map((s) => s.match(/\(#(\d+)\)\s*$/))
        .filter((m) => m !== null)
        .map((m) => Number(m[1])),
    ]),
  ]

  const mergedPrs = prNumbers.flatMap((num) => {
    try {
      return [shJson('gh', ['pr', 'view', String(num), '--json', 'number,title,body,url'])]
    } catch {
      return [] // a trailing "(#N)" that references an issue, not a PR
    }
  })

  const commitLines = sh('git', ['log', 'origin/main..origin/dev', '--pretty=format:- %s (`%h`)', '--no-merges'])
    .split('\n')
    .filter(Boolean)

  const version = readVersion()
  const previous = parsePreviousState(openPr?.body)
  const body = buildBody({ version, ahead, commitLines, mergedPrs, previous })
  const title = `${PROMOTION_TITLE_PREFIX} (${version}, ${ahead} commit${ahead === 1 ? '' : 's'})`
  
  // Calculate unchecked items for the draft state logic
  const prodLines = extractSection(body, '## Production promotion checklist')
  const prodUnchecked = (prodLines ? findUncheckedInLines(prodLines) : []).filter(t => !POST_MERGE_RE.test(t))
  
  const incLines = extractSection(body, '## Included changes')
  const uncheckedPrs = []
  if (incLines) {
    for (const [prNumber, { lines: prLines }] of splitIncludedByPr(incLines)) {
      if (findUncheckedInLines(prLines).length > 0) {
        uncheckedPrs.push(prNumber)
      }
    }
  }
  
  const hasUnchecked = prodUnchecked.length > 0 || uncheckedPrs.length > 0
  
  const commentMarker = '<!-- promotion-qa-trigger -->'
  let commentBody = commentMarker + '\n'
  if (hasUnchecked) {
    commentBody += '### ⚠️ QA pending\n\n'
    if (uncheckedPrs.length > 0) {
      commentBody += `Unfinished feature PR test plans: ${uncheckedPrs.map(n => `#${n}`).join(', ')} (${uncheckedPrs.length})\n\n`
    }
    commentBody += `This promotion PR is kept as a **draft** while items are unverified. Mark it **Ready for review** to run catch-up QA on staging for unchecked items.`
  } else {
    commentBody += '### ✅ All items verified\n\nAll included changes have QA ticks. Mark **Ready for review** when you intend to merge.'
  }

  const bodyFile = join(tmpdir(), `boop-watch-promotion-pr-${process.pid}.md`)
  writeFileSync(bodyFile, body)

  ensureLabel()

  if (openPr) {
    sh('gh', ['pr', 'edit', String(openPr.number), '--title', title, '--body-file', bodyFile])
    console.log(`Updated promotion PR #${openPr.number}`)
    if (hasUnchecked && !openPr.isDraft) {
      sh('gh', ['pr', 'ready', String(openPr.number), '--undo'])
      console.log(`Converted promotion PR #${openPr.number} back to draft (has unchecked items)`)
    }
    try {
      const comments = shJson('gh', ['api', `/repos/{owner}/{repo}/issues/${openPr.number}/comments`])
      const existing = comments.find(c => c.body && c.body.includes(commentMarker))
      if (existing) {
        sh('gh', ['api', '-X', 'PATCH', `/repos/{owner}/{repo}/issues/comments/${existing.id}`, '-f', `body=${commentBody}`])
      } else {
        sh('gh', ['pr', 'comment', String(openPr.number), '-b', commentBody])
      }
    } catch (e) {
      console.error(`Failed to update comment: ${e.message}`)
    }
    return
  }

  // No open promotion PR — create one. This is the only step that can be
  // denied: `GITHUB_TOKEN` can freely *edit* an existing PR (the branch above),
  // but *creating* one is gated by the org/repo "Allow GitHub Actions to create
  // and approve pull requests" policy. When that's off, don't fail the whole
  // workflow (the PR is recreated every promotion cycle, so this would go red on
  // every dev push) — hand the maintainer a ready-to-open PR instead.
  try {
    const url = sh('gh', [
      'pr', 'create',
      '--base', 'main',
      '--head', 'dev',
      '--title', title,
      '--body-file', bodyFile,
      '--label', PROMOTION_LABEL,
      ...(hasUnchecked ? ['--draft'] : [])
    ])
    console.log(`Created promotion PR: ${url}`)
    const prNumberMatch = url.match(/\/pull\/(\d+)/)
    if (prNumberMatch) {
      sh('gh', ['pr', 'comment', prNumberMatch[1], '-b', commentBody])
    }
  } catch (err) {
    const msg = String(err?.stderr || err?.message || err)
    if (!/not permitted to create or approve pull requests|createPullRequest/i.test(msg)) throw err
    reportManualCreateNeeded({ title, body, bodyFile })
  }
}

// The create policy is off: surface the intended PR so a human can open it with
// one click, and exit 0 so the workflow stays green.
function reportManualCreateNeeded({ title, body, bodyFile }) {
  const compareUrl = 'https://github.com/boophost/boop-watch/compare/main...dev?expand=1'
  console.log('::notice title=Promotion PR::GitHub Actions may not create PRs here; create the dev→main promotion PR manually.')
  console.log(`No open promotion PR, and Actions can't create one (org/repo policy). Open it manually:`)
  console.log(`  ${compareUrl}`)
  console.log(`  or: gh pr create --base main --head dev --title '${title}' --body-file <file> --label ${PROMOTION_LABEL}`)
  console.log('Once it exists, later dev pushes update it automatically.')

  const summaryPath = process.env.GITHUB_STEP_SUMMARY
  if (summaryPath) {
    const summary = [
      '## Promotion PR needs manual creation',
      '',
      "GitHub Actions isn't permitted to create PRs in this repo, so the rolling dev→main promotion PR",
      `couldn't be opened automatically. [**Create it here**](${compareUrl}), then paste the body below`,
      '(later `dev` pushes will keep it updated).',
      '',
      `**Title:** \`${title}\``,
      '',
      '<details><summary>PR body</summary>',
      '',
      '```markdown',
      body,
      '```',
      '',
      '</details>',
      '',
    ].join('\n')
    try {
      writeFileSync(summaryPath, summary)
    } catch {
      // step summary is best-effort
    }
  }
  console.log(`(PR body also written to ${bodyFile})`)
}

if (import.meta.url === `file://${process.argv[1]}`) main()
